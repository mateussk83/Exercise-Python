"""
Faça uma aplicação que peça dois numeros e some, subtrai, multiplique e divida os mesmos.
"""

num1 = float(input("Informe um numero: "))
num2 = float(input("Informe outro numero: "))

soma = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2
divisao = num1 / num2

print("A somas dos numeros é {0}".format(soma))
print("A subtração dos numeros é {0}".format(subtracao))
print("A multiplicação dos numeros é {0}".format(multiplicacao))
print("A divisão dos numeros é {0}".format(divisao))
