num = int(input("Informe o número correspondente ao dia da semana(1-Domingo, 2-Segunda...): "))

if num == 1:
    print("Este dia é Domingo!!")
elif num == 2:
    print("Este dia é Segunda!!")
elif num == 3:
    print("Este dia é Terça!!")
elif num == 4:
    print("Este dia é Quarta!!")
elif num == 5:
    print("Este dia é Quinta!!")
elif num == 6:
    print("Este dia é Sexta!!")
elif num == 7:
    print("Este dia é Sabado!!")
else:
    print("Informe algum numero Valido!!")